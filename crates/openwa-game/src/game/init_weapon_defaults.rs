//! Per-weapon defaults populator: ports WA's two unrolled-write
//! initialiser functions to Rust.
//!
//! - `InitWeaponDefaultsBaseline` (0x00537320, 1161 inst, no calls / no branches)
//! - `InitWeaponDefaultsExtended` (0x00539100, 1100 inst, no calls / no branches)
//!
//! Both are flat sequences of `MOV [EAX+offset], imm/reg` stores; merged
//! here into a single function that runs after the skeleton zeroes and
//! per-entry-inits the table (see [`crate::game::init_weapon_table`]).
//! Bit-identity vs the original asm was confirmed by side-by-side
//! `memcmp` across 597 worms2d replays (15 distinct `game_version`s,
//! both `cap` branches).
//!
//! Originally generated by `tools/port_init_weapon_defaults.py` (kept
//! committed for reference) and then refactored by hand. The script is
//! only useful if you ever need to re-derive from scratch.

